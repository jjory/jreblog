"""
field_map.py
─────────────────────────────────────────────────────────
매물 항목 한글 매핑 모듈 (비자 / 構造 / 方向 / 設備)

방식 (路線 시트와 동일한 "절충"):
- 자주 쓰는 용어는 이 파일에 기본 매핑으로 내장 → 지금 바로 작동
- 나중에 구글 시트(設備 등)에서 추가/덮어쓰기 가능 (apply_sheet_overrides)
  → 사장님이 시트만 채우면 코드 수정 없이 용어 추가/변경

노선·역·지명 번역은 기존 translation_db / sheet_sync(路線 시트)가 담당.
이 파일은 그 외 항목(비자·構造·方向·設備)만 담당한다.
"""

import re
from typing import Optional


# ──────────────────────────────────────────────────────
# 1) 비자 — 파일명 "매물번호-코드" 의 한글 코드로 판정
#    예: "0531001-워_도면.jpg" → "워" → 설명
#    코드가 없으면(하이픈 없음) None → 표에서 공란 처리 (절대 추측 안 함)
# ──────────────────────────────────────────────────────
VISA_MAP = {
    "워": "해외심사O · 모든비자O(워홀 포함)",
    "어": "해외심사O · 유학&취업O · 워홀X (어학)",
    "유": "해외심사O · 유학&취업O · 워홀X",
    "대": "해외심사O · 대학유학&취업O · 워홀X",
    "취": "해외심사O · 취업O · 유학&워홀X",
    "국": "국내심사만O",
    "국유": "국내심사 · 유학&취업O · 해외심사X",
    "국취": "국내심사 · 취업만O · 해외심사X",
}

# 긴 코드부터 매칭해야 "국유"가 "국"으로 잘리지 않음
_VISA_CODES_BY_LEN = sorted(VISA_MAP.keys(), key=len, reverse=True)


def extract_visa_code(filename: str) -> Optional[str]:
    """
    파일명에서 비자 코드만 추출. 형식: '매물번호-코드...'.
    예) '0531001-워_도면.jpg' → '워'
        '0531001-국유.pdf'    → '국유'
        '0531001_도면.jpg'    → None  (코드 없음 → 스킵)
    코드가 목록에 없으면 None.
    """
    if not filename:
        return None
    # 매물번호(앞 7자리) 다음에 하이픈이 와야 함
    m = re.match(r"^\d{7}-(.+)$", filename.strip())
    if not m:
        return None
    rest = m.group(1)
    # 하이픈 뒤에서 한글 코드 부분만 (확장자/언더스코어/공백 전까지)
    head = re.split(r"[_\.\s\-]", rest, 1)[0]
    # 알려진 코드와 매칭 (긴 것 우선)
    for code in _VISA_CODES_BY_LEN:
        if head.startswith(code):
            return code
    return None


def visa_text(filename: str) -> str:
    """파일명 → 비자 설명 문자열. 코드 없으면 '' (공란)."""
    code = extract_visa_code(filename)
    if not code:
        return ""
    return VISA_MAP.get(code, "")


# ──────────────────────────────────────────────────────
# 2) 構造(구조) 한글 매핑
# ──────────────────────────────────────────────────────
STRUCTURE_MAP = {
    "木造": "목조",
    "軽量鉄骨": "경량철골", "軽量鉄骨造": "경량철골",
    "重量鉄骨": "중량철골", "重量鉄骨造": "중량철골",
    "鉄骨造": "철골조", "鉄骨": "철골조",
    "鉄筋コンクリート造": "철근콘크리트조",
    "RC": "철근콘크리트조", "RC造": "철근콘크리트조",
    "鉄骨鉄筋コンクリート造": "철골철근콘크리트조",
    "SRC": "철골철근콘크리트조", "SRC造": "철골철근콘크리트조",
}


def structure_ko(jp: str) -> str:
    """構造 일본어 → 한글. 매핑 없으면 원문 그대로."""
    if not jp:
        return ""
    s = jp.strip()
    return STRUCTURE_MAP.get(s, s)


# ──────────────────────────────────────────────────────
# 3) 方向(방향) 한글 매핑
# ──────────────────────────────────────────────────────
DIRECTION_MAP = {
    "南": "남향", "北": "북향", "東": "동향", "西": "서향",
    "南東": "남동향", "南西": "남서향", "北東": "북동향", "北西": "북서향",
    "東南": "남동향", "西南": "남서향", "東北": "북동향", "西北": "북서향",
}


def direction_ko(jp: str) -> str:
    """方向 일본어 → 한글. '南向き' 처럼 '向き'가 붙어도 처리. 매핑 없으면 원문."""
    if not jp:
        return ""
    s = jp.strip().replace("向き", "").replace("向", "").strip()
    return DIRECTION_MAP.get(s, jp.strip())


# ──────────────────────────────────────────────────────
# 4) 設備(설비) 한글 매핑  ← (다) 절충: 기본 내장 + 시트 추가 가능
# ──────────────────────────────────────────────────────
FACILITY_MAP = {
    "エアコン": "에어컨",
    "バス・トイレ別": "화장실 욕실 분리",
    "オートロック": "오토록",
    "宅配ボックス": "택배박스",
    "都市ガス": "도시가스",
    "プロパン": "프로판가스", "プロパンガス": "프로판가스",
    "IH": "IH",
    "エレベーター": "엘리베이터",
    "室内洗濯機置き場": "실내 세탁기 설치",
    "独立洗面台": "독립세면대",
    "バルコニー": "발코니",
    "インターネット無料": "인터넷 무료",
    "TVモニターホン": "비디오폰", "モニター付きインターホン": "비디오폰",
    "24時間ゴミ出し": "24시간 쓰레기 배출",
    "ペット可": "반려동물 가능",
    "楽器可": "악기 가능",
}

# 시트에서 읽어온 추가 매핑을 여기에 병합 (런타임에 채워짐)
_FACILITY_SHEET_OVERRIDES = {}


def apply_facility_sheet_overrides(mapping: dict) -> None:
    """
    구글 시트(設備 시트)에서 읽어온 {일본어: 한글} 매핑을 추가/덮어쓰기.
    (다) 절충 방식: 기본 내장값 위에 시트값을 덮어씀.
    시트가 없으면 호출 안 됨 → 기본 매핑만 사용 (앱은 항상 정상).
    """
    if mapping:
        _FACILITY_SHEET_OVERRIDES.update(mapping)


def facility_ko(jp: str) -> str:
    """設備 일본어 → 한글. 시트 추가분 우선, 없으면 기본, 그것도 없으면 원문."""
    if not jp:
        return ""
    s = jp.strip()
    if s in _FACILITY_SHEET_OVERRIDES:
        return _FACILITY_SHEET_OVERRIDES[s]
    return FACILITY_MAP.get(s, s)


